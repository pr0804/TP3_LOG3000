"""
operators.py - Opérations mathématiques

Ce fichier contient les 4 opérations de base de la calculatrice:
addition, soustraction, multiplication et division.
"""

def add(a,b):
    # Additionne deux nombres et retourne le résultat
    return a + b

def subtract(a,b):
    # Soustrait deux nombres et retourne le résultat
    return a - b

def multiply(a,b):
    # Multiplie deux nombres et retourne le résultat
    return a * b

def divide(a,b):
    # Divise deux nombres et retourne le résultat
    return a / b
